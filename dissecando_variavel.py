# Salva Valor
a = input('Digite Algo: ')
# Mostra Tipos Primitivos
print('O Tipo Primitivo  desse Valor é = ', type(a))
# Identifica Espaços
print('Só Tem espaços?', a.isspace())
# Identifica se é Numérico
print('Só Tem Números?', a.isnumeric())
# Identifica se é Alfanumérico
print('É Alfanumérico?', a.isalnum())
# Identifica se é Maiúsculo
print('É Maiúsculo?', a.isupper())
# Identifica se é Minúsculo
print('É Minúsculo?', a.islower())
# Identifica se está Capitalizada
print('Está Capitalizada?', a.istitle())

